#include<iostream>
#include<cmath>

using namespace std;

int main(){
	//power x^y (y is the power of x)
	cout<<pow(2,3)<<endl;

	// sqaure root
	cout<<sqrt(36)<<endl;

	//round off
	cout<<round(4.4)<<endl;

	// round off till next highest
	cout<<ceil(4.1)<<endl;

	// round off previous lowest value
	cout<<floor(4.8)<<endl;

	// Max of two number
	cout<<fmax(3,10)<<endl;

	// Min of two number
	cout<<fmin(3,10)<<endl;

	return 0;
}
